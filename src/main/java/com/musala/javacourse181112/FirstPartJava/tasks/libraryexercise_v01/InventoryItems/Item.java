package com.musala.javacourse181112.FirstPartJava.tasks.libraryexercise_v01.InventoryItems;

public abstract class Item {
    protected int id;
    protected String ISBN;
    protected String name;
    protected int pages;

    public Item() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getISBN() {
        return ISBN;
    }

    public void setISBN(String ISBN) {
        this.ISBN = ISBN;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPages() {
        return pages;
    }

    public void setPages(int pages) {
        this.pages = pages;
    }
}
